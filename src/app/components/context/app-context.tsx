const AppContext = createContext<AppContextType | null>(null);

/**
 * Root application state provider.
 * Manages page navigation, notifications, activities, theme config,
 * onboarding status, and mobile sidebar state.
 * Persists serializable subset to `localStorage`.
 */
export function AppProvider({ children }: { children: ReactNode }) {
  const persisted = loadPersistedState();

  const [activePage, setActivePageRaw] = useState<PageId>(persisted.activePage || 'dashboard');
  const [appMode, setAppModeRaw] = useState<AppMode>(persisted.appMode || 'standalone');
  const [sidebarPinned, setSidebarPinnedRaw] = useState(persisted.sidebarPinned ?? false);
  const [notifications, setNotifications] = useState<NotificationItem[]>(initialNotifications);
  const [recentActivities, setRecentActivities] = useState<ActivityItem[]>(initialActivities);
  const [theme, setTheme] = useState<ThemeConfig>(loadTheme);
  const [onboardingDone, setOnboardingDoneRaw] = useState(loadOnboardingDone);
  const [mobileSidebarOpen, setMobileSidebarOpen] = useState(false);

  // Persist state changes
  useEffect(() => {
    savePersistedState({ activePage, appMode, sidebarPinned });
  }, [activePage, appMode, sidebarPinned]);

  // Persist theme changes
  useEffect(() => {
    saveTheme(theme);
  }, [theme]);

  const setActivePage = useCallback((page: PageId) => {
    setActivePageRaw(page);
  }, []);

  const setAppMode = useCallback((mode: AppMode) => {
    setAppModeRaw(mode);
  }, []);

  const setSidebarPinned = useCallback((pinned: boolean) => {
    setSidebarPinnedRaw(pinned);
  }, []);

  const addNotification = useCallback((n: Omit<NotificationItem, 'id' | 'timestamp' | 'read'>) => {
    setNotifications(prev =>
      [
        {
          ...n,
          id: `n_${Date.now()}`,
          timestamp: new Date(),
          read: false,
        },
        ...prev,
      ].slice(0, 50),
    );
  }, []);

  const markNotificationRead = useCallback((id: string) => {
    setNotifications(prev => prev.map(n => (n.id === id ? { ...n, read: true } : n)));
  }, []);

  const clearNotifications = useCallback(() => {
    setNotifications(prev => prev.map(n => ({ ...n, read: true })));
  }, []);

  const addActivity = useCallback((a: Omit<ActivityItem, 'id' | 'timestamp'>) => {
    setRecentActivities(prev =>
      [
        {
          ...a,
          id: `a_${Date.now()}`,
          timestamp: new Date(),
        },
        ...prev,
      ].slice(0, 20),
    );
  }, []);

  const updateTheme = useCallback((partial: Partial<ThemeConfig>) => {
    setTheme(prev => ({ ...prev, ...partial }));
  }, []);

  const resetTheme = useCallback(() => {
    setTheme({ ...defaultTheme });
  }, []);

  const setOnboardingDone = useCallback((done: boolean) => {
    setOnboardingDoneRaw(done);
    try {
      localStorage.setItem(ONBOARDING_KEY, String(done));
    } catch {
      /* ignore */
    }
  }, []);

  const neonAlpha = useCallback(
    (base: number) => {
      return base * (theme.neonIntensity / 100);
    },
    [theme.neonIntensity],
  );

  const unreadCount = notifications.filter(n => !n.read).length;

  return (
    <AppContext.Provider
      value={{
        activePage,
        appMode,
        sidebarPinned,
        notifications,
        recentActivities,
        theme,
        onboardingDone,
        mobileSidebarOpen,
        setActivePage,
        setAppMode,
        setSidebarPinned,
        addNotification,
        markNotificationRead,
        clearNotifications,
        addActivity,
        unreadCount,
        updateTheme,
        resetTheme,
        setOnboardingDone,
        setMobileSidebarOpen,
        neonAlpha,
      }}
    >
      {children}
    </AppContext.Provider>
  );
}

/**
 * Hook to access the global application state and dispatch actions.
 * Must be called within an `<AppProvider>` tree.
 *
 * @throws Error if called outside of `AppProvider`.
 */
export function useApp() {
  const ctx = useContext(AppContext);
  if (!ctx) throw new Error('useApp must be used within AppProvider');
  return ctx;
}

