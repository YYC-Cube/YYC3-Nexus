/**
 * Simulates WebSocket-like realtime data pushes.
 * Periodically injects notifications (every 15-25 s) and activities
 * (every 8-15 s) from a rotating pool of mock data.
 */
export function useRealtimeSimulation() {
  const { addNotification, addActivity } = useApp();
  const notifIdxRef = useRef(0);
  const actIdxRef = useRef(0);

  useEffect(() => {
    // Push a random notification every 15-25s
    const notifTimer = setInterval(
      () => {
        const item = realtimeNotifPool[notifIdxRef.current % realtimeNotifPool.length];
        addNotification(item);
        notifIdxRef.current++;
      },
      15000 + Math.random() * 10000,
    );

    // Push a random activity every 8-15s
    const actTimer = setInterval(
      () => {
        const item = realtimeActivityPool[actIdxRef.current % realtimeActivityPool.length];
        addActivity(item);
        actIdxRef.current++;
      },
      8000 + Math.random() * 7000,
    );

    return () => {
      clearInterval(notifTimer);
      clearInterval(actTimer);
    };
  }, [addNotification, addActivity]);
}

