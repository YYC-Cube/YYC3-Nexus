const STORAGE_KEY = 'yyc3_app_state';
const THEME_STORAGE_KEY = 'yyc3_theme';
const ONBOARDING_KEY = 'yyc3_onboarding_done';

// Serializable subset for localStorage
interface PersistedState {
  activePage: PageId;
  appMode: AppMode;
  sidebarPinned: boolean;
}

function loadPersistedState(): Partial<PersistedState> {
  try {
    const raw = localStorage.getItem(STORAGE_KEY);
    if (raw) {
      const parsed = JSON.parse(raw);
      return {
        activePage: parsed.activePage || 'dashboard',
        appMode: parsed.appMode || 'standalone',
        sidebarPinned: parsed.sidebarPinned ?? false,
      };
    }
  } catch {
    // ignore parse errors
  }
  return {};
}

function savePersistedState(state: PersistedState) {
  try {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(state));
  } catch {
    // ignore storage errors
  }
}

function loadTheme(): ThemeConfig {
  try {
    const raw = localStorage.getItem(THEME_STORAGE_KEY);
    if (raw) return { ...defaultTheme, ...JSON.parse(raw) };
  } catch {
    /* ignore */
  }
  return { ...defaultTheme };
}

function saveTheme(theme: ThemeConfig) {
  try {
    localStorage.setItem(THEME_STORAGE_KEY, JSON.stringify(theme));
  } catch {
    /* ignore */
  }
}

function loadOnboardingDone(): boolean {
  try {
    return localStorage.getItem(ONBOARDING_KEY) === 'true';
  } catch {
    return false;
  }
}

