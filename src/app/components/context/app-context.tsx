/**
 * Live KPI data structure returned by {@link useLiveKPI}.
 * Values fluctuate slightly every 5 seconds to simulate realtime updates.
 */
export interface LiveKPI {
  customers: number;
  calls: number;
  aiTasks: number;
  responseMs: number;
  refreshKey: number; // increments on each update for animation trigger
}

/**
 * Hook returning slightly fluctuating KPI metrics, updating every 5 s.
 * Useful for dashboard live counters and sparkline animations.
 */
export function useLiveKPI(): LiveKPI {
  const [kpi, setKpi] = useState<LiveKPI>({
    customers: 1388,
    calls: 247,
    aiTasks: 1892,
    responseMs: 12,
    refreshKey: 0,
  });

  useEffect(() => {
    const timer = setInterval(() => {
      setKpi(prev => ({
        customers: prev.customers + Math.floor(Math.random() * 3) - (Math.random() > 0.7 ? 1 : 0),
        calls: prev.calls + Math.floor(Math.random() * 4),
        aiTasks: prev.aiTasks + Math.floor(Math.random() * 8) + 1,
        responseMs: Math.max(8, Math.min(18, prev.responseMs + (Math.random() > 0.5 ? 1 : -1))),
        refreshKey: prev.refreshKey + 1,
      }));
    }, 5000);
    return () => clearInterval(timer);
  }, []);

  return kpi;
}

